public class App {
    public static void main(String[] args) throws Exception {
        System.out.println("Estrutura de controle 'for'.");
        System.out.println("inicialização, condição, incremento.");

        // Enquanto i for menor que 11, vou incrementar 1 número a mais.

        for (int i = 1; i < 11; i++) {
            System.out.println(i);
        }
    }
}
